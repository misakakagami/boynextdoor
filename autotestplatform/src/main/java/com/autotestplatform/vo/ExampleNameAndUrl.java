package com.autotestplatform.vo;

import java.io.Serializable;

public class ExampleNameAndUrl implements Serializable {

    /** 
    * @Fields serialVersionUID
    */
    private static final long serialVersionUID = 3703337980849153870L;

    private String            exampleName;

    private String            exampleUrl;

    public ExampleNameAndUrl(String exampleName, String exampleUrl) {
        super();
        this.exampleName = exampleName;
        this.exampleUrl = exampleUrl;
    }

    public ExampleNameAndUrl() {
        super();
    }

    public String getExampleName() {
        return exampleName;
    }

    public void setExampleName(String exampleName) {
        this.exampleName = exampleName;
    }

    public String getExampleUrl() {
        return exampleUrl;
    }

    public void setExampleUrl(String exampleUrl) {
        this.exampleUrl = exampleUrl;
    }

}
