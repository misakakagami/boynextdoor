package com.autotestplatform.vo;

import java.io.Serializable;

public class ScriptNameAndUrl implements Serializable {

    /** 
    * @Fields serialVersionUID
    */
    private static final long serialVersionUID = 3703337980849153870L;

    private String            scriptName;

    private String            scriptUrl;

    public ScriptNameAndUrl(String scriptName, String scriptUrl) {
        super();
        this.scriptName = scriptName;
        this.scriptUrl = scriptUrl;
    }

    public ScriptNameAndUrl() {
        super();
    }

    public String getScriptName() {
        return scriptName;
    }

    public void setScriptName(String scriptName) {
        this.scriptName = scriptName;
    }

    public String getScriptUrl() {
        return scriptUrl;
    }

    public void setScriptUrl(String scriptUrl) {
        this.scriptUrl = scriptUrl;
    }
}
